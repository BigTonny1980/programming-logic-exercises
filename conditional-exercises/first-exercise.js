let anyNumber = 5;

let verification = Math.sign(anyNumber);

if (verification === 1) {
   console.log("The number is positive");
}

if (verification === -1) {
   console.log("The number is negative");
}

if (verification === 0) {
   console.log("The number is zero");
}

